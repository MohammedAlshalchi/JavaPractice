package day23_Method;

public class EachElement {
    /*
 create a method named printEachElement that can print each elements of an integer array
     */
    public static void main( String [] args ) {
     printEachElementArray( new int[] {2,3,5,7} );



    }

    public static void printEachElementArray ( int [] arr){
        for ( int each : arr ) {
            System.out.print( each + " " );

        }

    }
















































}
